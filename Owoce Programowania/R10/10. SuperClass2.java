public class SuperClass2
{
   /**
      Konstruktor numer 1.
   */

   public SuperClass2()
   {
      System.out.println("To konstruktor bezargumentowy" +
                         "klasy bazowej.");
   }

   /**
      Konstruktor numer 2.
   */

   public SuperClass2(int arg)
   {
      System.out.println("Do konstruktora " +
                        "klasy bazowej przekazano " +
                        "następujący argument: " + arg);
   }
}
